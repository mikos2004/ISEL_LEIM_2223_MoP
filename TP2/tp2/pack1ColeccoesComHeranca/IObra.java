package tp2.pack1ColeccoesComHeranca;

/**
 * Interface que define os métodos abstratos de uma obra.
 */
public interface IObra {

    /**
     * Retorna o número de páginas da obra.
     * @return o número de páginas da obra.
     */
    int getNumPaginas();

    /**
     * Retorna o preço da obra.
     * @return o preço da obra.
     */
    float getPreco();
}